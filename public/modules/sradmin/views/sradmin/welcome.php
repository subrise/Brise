		<h1>Welcome at Brise</h1>
	</header>

	<p>Brise is a young content management system, that tries to simplify managing your web content.</p>

	<p>It does this by trying to merge the backend with your frontend. In other words, it tries to give you a What You See Is What You Get (WYSIWYG) editor. Users can create pages that are based on certain templates which are created by the designer. These templates can contain different areas that the user can edit or add content to. The user may do so by assigning widgets to areas.</p>

	<p>Widgets will come in various types:</p>

	<ul>
		<li>Strings</li>
		<li>Text blocks</li>
		<li>Images</li>
		<li>Videos</li>
		<li>Blog feeds</li>
		<li>etc.</li>
	</ul>
